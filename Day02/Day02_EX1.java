// 정수형 변수 선언, 대입 
/*
 * 주석을 여러줄로 달 수 있다. 
 */
public class Day02_EX1 {

	public static void main(String[] args) {
		int apple; // 정수형 (정수를 담을 수 있는) 변수 선언
		apple = 7; // 7 대
		
		int banana = 17; // 한 줄로 선언, 대입 
		banana = 30; // 17 --> 30 
		System.out.println(banana);
		// ""따옴표는 글자 출력, 따옴표를 빼면 banana의 값을 출력한다. 
	}

}
